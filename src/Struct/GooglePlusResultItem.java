package Struct;

public class GooglePlusResultItem {
    public String title;
    public String published;
    public String url;
    public String actorid;
    public String actordisplayName;
    public String imageurl;
    public String objectcontent;

    public GooglePlusResultItem(String title, String published, String url, String actorid, String actordisplayName, String imageurl, String objectcontent) {
        this.title = title;
        this.published = published;
        this.url = url;
        this.actorid = actorid;
        this.actordisplayName = actordisplayName;
        this.imageurl = imageurl;
        this.objectcontent = objectcontent;
    }

    
}
